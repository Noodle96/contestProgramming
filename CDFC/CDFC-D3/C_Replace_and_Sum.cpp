void solve() {
    int n,q; cin >> n >> q;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
    }
    a[n-1] = max(b[n-1], a[n-1]);
    for(int i = n-2; i >= 0; i--){
        a[i] = max(a[i], max(b[i], a[i + 1]));
    }
    vector<int> prefixSum(n + 1, 0);
    for(int i = 1 ; i <= n ; i++){
        prefixSum[i] = prefixSum[i - 1] + a[i - 1];
    }
    while(q--){
        int l,r; cin >> l >> r;
        cout << prefixSum[r] - prefixSum[l - 1] << " ";
    }cout << "\n";
}

